package dtaTypes;

public class numberAndNonDecimal {

	public static void main(String[] args) {
		// there are four types of data are used in number with nondecimal
		//1.byte  2.short  3.int  4.long
		
		//1. variable declaration
		byte number;
		
		//2. variable initialization (assign value)
		number=123;
		
		//3.usage
		System.out.println("value of number is "+number);
		
		//1. variable declaration
		short num1;
		
		//2. variable initialization (assign value)
		num1=31000;
		
		//3.usage
		System.out.println("value of num1 is "+num1);
		

	}

}
