package dtaTypes;

public class numberWithDecimal {

	public static void main(String[] args) {
		// there are two types of data are used in number with decimal
		//1.float  2.double
		
		//1. variable declaration
		float Height;
		
		//2. variable initialization (assign value)
		Height=6.123456789123f;
		
		//3. usage
		System.out.println("my height is "+Height+" ft.");
		
		//1. variable declaration
		double distance;
		
		//2. variable initialization (assign value)
		distance=9.12345678912345d;
		
		//3. usage
		System.out.println("distance is "+distance+" km");

	}

}
