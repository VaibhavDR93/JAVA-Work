package Loops;

public class ForLoop {

	public static void main(String[] args) {
		// i want to print number 0-10 and incriment by 1
		
		for (int a = 0; a<=10; a++)
		{
			System.out.println(a);
		}
		System.out.println("<<<<<<<<<<<<<<<<<<>>>>>>>>>>>>>>>>");
		
		for (int a=10; a>=0; a--)
		{
			System.out.println(a);
		}
		System.out.println("<<<<<<<<<<<<<<<<<<>>>>>>>>>>>>>>>>");
		//i want to print 2s table
		for (int i = 2; i<=20; i=i+2)
		{
			System.out.println(i);
		}
		System.out.println("<<<<<<<<<<<<<<<<<<>>>>>>>>>>>>>>>>");
		// i want to print 6  table
		for (int q = 60; q>=6; q=q-6) 
		{
			System.out.println(q);
		}
		System.out.println("<<<<<<<<<<<<<<<<<<>>>>>>>>>>>>>>>>");
		//i want to print 6 time vaibhav
		for (int i = 1; i <=6 ; i++) 
		{
			System.out.println("vaibhav");
		}

	}

}
