package Methods;

public class MathOperationDiff
{

	public void mul()// non static method
	{
		int a=5;
		int b=10;
		int mult=a*b;
		System.out.println("mul is "+mult);
	}
	
	public static void div()//static method
	{
		int a=10;
		int b=5;
		int divi=a/b;
		System.out.println("div is "+divi);
		
	}
}
