package Casting;

public class ImplicitCasting
{
	//converting lower data type info to higher data type info is  callerd implicit casting
	//it is also called widening casting
	//where memory size goes on increasing there is no data loss

	public static void main(String[] args)
	{
		int a=50;
		System.out.println(a);
		
		double b=a;
		System.out.println(b);

	}

}
