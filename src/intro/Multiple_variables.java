package intro;

public class Multiple_variables {

	public static void main(String[] args) {
		// name--> Vaibhav---> string
		//age--> 25.5---> float
		//rollNum--->50---> int
		//grade--->A---> char
		
		//1. variable declaration
		String name;
		
		//2.variable initialization--->assign value
		name="Vaibhav";
		
		//3. usage
		System.out.println("my name is "+name+",");
		
		//1. variable declaration
		float age;
		
		//2.variable initialization--->assign value
		age=25.6f;
		
		//3. usage
		System.out.println("my age is "+age+" year,");
		
		//1. variable declaration
		int rollNum;
		
		//2.variable initialization--->assign value
		rollNum=50;
		
		//3. usage
		System.out.println("my roll Number is "+rollNum+",");
		
		
		//1. variable declaration
		char grade;
		
		//2.variable initialization--->assign value
		grade='A';
		
		//3. usage
		System.out.println("my grade is "+grade+",");
		
		

	}

}
